def check_vowels():
    nombre= input("Ingrese un nombre: ")

    nombre_final= nombre.lower()
    print (f"Contiene a:", "a" in nombre_final)
    print (f"Contiene e:", "e" in nombre_final)
    print (f"Contiene i:", "i" in nombre_final)
    print (f"Contiene o:", "o" in nombre_final)
    print (f"Contiene u:", "u" in nombre_final)


    
